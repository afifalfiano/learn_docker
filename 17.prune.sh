Prune
Saat kita menggunakan docker ada hal hal yang tidak digunakan misal container sudah distop, image yang tidak digunakan container, volume yang tidak digunakan
Hampi semua perintah docker mendukung perintah prune


Docker Image
docker image prune

Docker Container
docker container prune

Docker Volume
docker volume prune 

Docker Network
docker network prune

Atau bisa menghapus semua netowork, volume, container dan image
docker system prune